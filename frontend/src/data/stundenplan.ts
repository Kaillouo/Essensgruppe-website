// Helper to parse week/notiz field
function w(note: string): { week: 'A' | 'B' | 'AB'; halbjahr?: 'Hj1' | 'Hj2' } {
  const week: 'A' | 'B' | 'AB' = note.includes('AB') || note.includes('Hj') ? 'AB'
    : note.startsWith('A') ? 'A'
    : note.startsWith('B') ? 'B'
    : 'AB';
  const halbjahr = note.includes('Hj1') ? 'Hj1' as const
    : note.includes('Hj2') ? 'Hj2' as const
    : undefined;
  return { week, halbjahr };
}

